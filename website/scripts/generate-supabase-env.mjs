import nextEnv from "@next/env";
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const { loadEnvConfig } = nextEnv;

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const websiteRoot = path.resolve(scriptDir, "..");
const repoRoot = path.resolve(websiteRoot, "..");
const outDir = path.join(websiteRoot, "lib", "generated");
const outFile = path.join(outDir, "supabase-env.ts");

loadEnvConfig(repoRoot);
loadEnvConfig(websiteRoot);

function pick(...names) {
  for (const name of names) {
    const value = process.env[name]?.trim();
    if (value) return value;
  }
  return undefined;
}

const url = pick("NEXT_PUBLIC_SUPABASE_URL", "EXPO_PUBLIC_SUPABASE_URL");
const key = pick(
  "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  "EXPO_PUBLIC_SUPABASE_ANON_KEY",
  "SUPABASE_ANON_KEY",
);

if (!url || !key) {
  console.error("\n❌ Supabase env missing for website build/dev.");
  console.error("   Set NEXT_PUBLIC_SUPABASE_URL + NEXT_PUBLIC_SUPABASE_ANON_KEY");
  console.error("   Local: repo root .env or website/.env.local");
  console.error("   Vercel: Project Settings → Environment Variables → redeploy\n");
  process.exit(1);
}

mkdirSync(outDir, { recursive: true });
writeFileSync(
  outFile,
  `// Auto-generated by scripts/generate-supabase-env.mjs — do not edit.\n` +
    `export const SUPABASE_URL = ${JSON.stringify(url)};\n` +
    `export const SUPABASE_ANON_KEY = ${JSON.stringify(key)};\n`,
);

console.log("✓ Generated lib/generated/supabase-env.ts");
