import nextEnv from "@next/env";
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const { loadEnvConfig } = nextEnv;

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const websiteRoot = path.resolve(scriptDir, "..");
const repoRoot = path.resolve(websiteRoot, "..");
const outDir = path.join(websiteRoot, "lib", "generated");
const outFile = path.join(outDir, "supabase-env.ts");

// Capture Vercel/shell env FIRST — loadEnvConfig only fills gaps from .env files.
const fromShell = {
  url: (process.env.NEXT_PUBLIC_SUPABASE_URL || process.env.EXPO_PUBLIC_SUPABASE_URL || "").trim(),
  key: (
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ||
    process.env.EXPO_PUBLIC_SUPABASE_ANON_KEY ||
    process.env.SUPABASE_ANON_KEY ||
    ""
  ).trim(),
};

loadEnvConfig(repoRoot);
loadEnvConfig(websiteRoot);

function pick(...names) {
  for (const name of names) {
    const value = process.env[name]?.trim();
    if (value) return value;
  }
  return undefined;
}

const url = fromShell.url || pick("NEXT_PUBLIC_SUPABASE_URL", "EXPO_PUBLIC_SUPABASE_URL");
const key =
  fromShell.key ||
  pick("NEXT_PUBLIC_SUPABASE_ANON_KEY", "EXPO_PUBLIC_SUPABASE_ANON_KEY", "SUPABASE_ANON_KEY");

if (!url || !key) {
  console.error("\n❌ Supabase env missing for website build/dev.");
  console.error("   Required: NEXT_PUBLIC_SUPABASE_URL + NEXT_PUBLIC_SUPABASE_ANON_KEY");
  console.error("   On Vercel: set them on the auditur-ruby project (Production), then Redeploy.");
  console.error("   Local: website/.env.local or repo root .env\n");
  process.exit(1);
}

mkdirSync(outDir, { recursive: true });
writeFileSync(
  outFile,
  `// Auto-generated by scripts/generate-supabase-env.mjs — do not edit by hand.\n` +
    `// Build stamp helps verify production is serving a fresh deploy.\n` +
    `export const SUPABASE_BUILD_STAMP = ${JSON.stringify(new Date().toISOString())};\n` +
    `export const SUPABASE_URL = ${JSON.stringify(url)};\n` +
    `export const SUPABASE_ANON_KEY = ${JSON.stringify(key)};\n`,
);

const host = (() => {
  try {
    return new URL(url).host;
  } catch {
    return "invalid-url";
  }
})();

console.log(
  `✓ Generated lib/generated/supabase-env.ts (host=${host}, keyPrefix=${key.slice(0, 16)}…, from=${fromShell.url && fromShell.key ? "shell/vercel" : "dotenv"})`,
);
